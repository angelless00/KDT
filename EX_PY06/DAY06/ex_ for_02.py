## 메세지를 입력 받습니다.
## 알파벳 대문자인경우 소문자로, 소문자인경우 대문자로
## 나머지는 그대로 되도록 출력하기
#-----------------------------------------------------------------------------------------------
msg=input('메세지를 입력하세요 : ')
for i in msg:
    if 'a'<=i<='z':
        print(chr(ord(i)-32),end='')
    elif 'A'<=i<'Z':
        print(chr(ord(i)+32),end='')
    else:
        print(i,end='')


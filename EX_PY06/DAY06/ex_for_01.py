## [실습]---------------------------------------------------------------------
## 숫자 1~50까지의 데이터가 있습니다.
## 해당 데이터에서 3의 배수는 제곱을 하고, 나머지 숫자는 그대로 해서 합계를 출력하세요.
#-----------------------------------------------------------------------------------------------
sum=0
for i in range(1,51):
    if i%3==0:
        sum=sum+i**2
    else:
        sum=sum+i
print(f'합계 :{sum}')
        
## [실습]---------------------------------------------------------------------
## 메세지에서 알파벳과 숫자를 구분해서 처리합니다.
## 알파벳은 ★, 숫자는 ♡로 변경해서 출력해주세요.
#-----------------------------------------------------------------------------------------------
msg='Good 2024'
for i in msg:
    if 'a'<=i<='z' or 'A'<=i<='Z':
        print('★',end='')
    elif '0'<=i<='9':
        print('♡',end='')
    else:
        print(' ',end='')





#-----------------------------------------------------------------------------------------------
# 문자열 출력 형태
#-----------------------------------------------------------------------------------------------
num=12

print(f'{num:>10}입니다.')
print(f'{num:^10}입니다.')
print(f'{num:<10}입니다.')
print(f'{num:0>10}입니다.')
print(f'{num:0^10}입니다.')
print(f'{num:0<10}입니다.')
#-----------------------------------------------------------------------------------------------
# 함수기능 : 정수를 덧셈한 후 결과를 반환하는 함수
# 함수이름 : addint
# 매개변수 : 0개~N개 즉, 가변인자 *num
# 함수결과 : 정수 result
#-----------------------------------------------------------------------------------------------
def addint(*nums):
    total=0
    for i in nums:
        total=total+i
    return total









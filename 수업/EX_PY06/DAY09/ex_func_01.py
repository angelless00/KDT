# 함수(Function) 이해 및 활용
#-----------------------------------------------------------------------------------------------
# 함수기능 : 3개의 정수를 덧셈결과를 반환하는 함수
# 함수이름 : add3
# 매개변수 : num1,num2,num3
# 함수결과 : int, result
#-----------------------------------------------------------------------------------------------
def add3(num1,num2,num3):
    result=num1+num2+num3
    return result

#-----------------------------------------------------------------------------------------------
# 함수기능 : 3개의 정수를 곱셈결과를 반환하는 함수
# 함수이름 : multi3
# 매개변수 : num1,num2,num3
# 함수결과 : int, result
#-----------------------------------------------------------------------------------------------
def multi3(num1,num2,num3):
    result=num1*num2*num3
    return result

#-----------------------------------------------------------------------------------------------
# 함수기능 : 2개의 정수를 나눗셈결과를 반환하는 함수
# 함수이름 : div
# 매개변수 : num1,num2
# 함수결과 : float, result
#-----------------------------------------------------------------------------------------------
